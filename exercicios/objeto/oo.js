var texto = "O número de telefone é (123) 456-7890";
var padrao = /\((\d{3})\) (\d{3})-(\d{4})/;
var resultado = texto.match(padrao);
console.log(resultado[1]);  // Saída: "123"
console.log(resultado[2]);  // Saída: "456"
console.log(resultado[3]);  // Saída: "7890"









































  


  


 
 
 

  